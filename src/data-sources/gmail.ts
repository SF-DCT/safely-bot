/**
 * 未読メールを取得し、一次フィルタ後のメールをClaudeに分析させるための文字列を返す
 */
export async function getUnreadEmails(
  maxResults: number = 20,
): Promise<string> {
  if (!GMAIL_CONFIG.refreshToken) {
    return "Gmail APIが未設定です。GMAIL_REFRESH_TOKEN を .env に追加してください。";
  }

  const accessToken = await getAccessToken();
  const { emails, filtered } = await fetchMessages(
    accessToken,
    "is:unread in:inbox newer_than:2d",
    maxResults,
  );

  if (emails.length === 0 && filtered.length === 0) {
    return "直近2日間の未読メールはありません。";
  }

  const lines: string[] = [];

  // フィルタ済みサマリー
  if (filtered.length > 0) {
    lines.push(
      `【自動除外】${filtered.length}件のメルマガ/通知を除外しました：`,
    );
    for (const f of filtered) {
      lines.push(`  • ${f.subject}（${f.reason}）`);
    }
    lines.push("");
  }

  if (emails.length === 0) {
    lines.push("返信候補のメールはありません。除外されたメールはすべてメルマガ/通知でした。");
    return lines.join("\n");
  }

  // 要対応メール
  lines.push(`【要確認メール】${emails.length}件\n`);

  emails.forEach((email, i) => {
    lines.push(
      `--- メール${i + 1} (ID: ${email.id}) ---`,
      `差出人: ${email.from}`,
      `件名: ${email.subject}`,
      `日時: ${email.date}`,
      `本文:\n${email.body}`,
      "",
    );
  });

  lines.push(
    "\n以上のメールを分析し、以下のフォーマットで回答してください：",
    "",
    "【返信が必要なメール】",
    "各メールについて：",
    "• メールID: （IDをそのまま記載）",
    "• 優先度（🔴高 / 🟡中 / 🟢低）",
    "• 理由（なぜ返信が必要か）",
    "• 返信案（そのまま使える丁寧な日本語の返信文）",
    "",
    "【返信不要のメール】",
    "• メール件名 — 不要な理由（社内メンバー→Slack連絡 / 自動通知 等）",
    "",
    "※ 判断はシステムプロンプトのビジネスコンテキスト（組織情報・連絡手段・判断基準）に従うこと。",
    "※ ユーザーが「送って」「返信して」と言ったら send_gmail_reply ツールを使ってそのまま送信できます。",
  );

  return lines.join("\n");
}

/**
 * 指定メールへの返信下書きをGmailに作成する
 */
export async function createGmailDraft(
  messageId: string,
  replyBody: string,
): Promise<string> {
  if (!GMAIL_CONFIG.refreshToken) {
    return "Gmail APIが未設定です。GMAIL_REFRESH_TOKEN を .env に追加してください。";
  }

  const accessToken = await getAccessToken();

  const msgUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages/${messageId}?format=full`;
  const msgRes = await fetch(msgUrl, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  const msgData = (await msgRes.json()) as {
    threadId: string;
    payload?: { headers?: Array<{ name: string; value: string }> };
  };

  const headers = msgData.payload?.headers || [];
  const from = extractHeader(headers, "From");
  const subject = extractHeader(headers, "Subject");
  const messageIdHeader = extractHeader(headers, "Message-ID");
  const threadId = msgData.threadId;

  const replySubject = subject.startsWith("Re:") ? subject : `Re: ${subject}`;
  const rawEmail = buildRawEmail(from, replySubject, replyBody, messageIdHeader);
  const encodedMessage = encodeRawEmail(rawEmail);

  const draftUrl = "https://gmail.googleapis.com/gmail/v1/users/me/drafts";
  const draftRes = await fetch(draftUrl, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${accessToken}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      message: { raw: encodedMessage, threadId },
    }),
  });

  const draftData = (await draftRes.json()) as {
    id?: string;
    error?: unknown;
  };

  if (draftData.error) {
    throw new Error(`Draft creation error: ${JSON.stringify(draftData.error)}`);
  }

  return [
    "✅ Gmailに返信の下書きを作成しました。",
    `宛先: ${from}`,
    `件名: ${replySubject}`,
    `Draft ID: ${draftData.id}`,
    "",
    "Gmailの下書きフォルダから確認・送信してください。",
  ].join("\n");
}

/**
 * 指定メールへの返信を直接送信する
 */
export async function sendGmailReply(
  messageId: string,
  replyBody: string,
): Promise<string> {
  if (!GMAIL_CONFIG.refreshToken) {
    return "Gmail APIが未設定です。GMAIL_REFRESH_TOKEN を .env に追加してください。";
  }

  const accessToken = await getAccessToken();

  // 元メール情報を取得
  const msgUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages/${messageId}?format=full`;
  const msgRes = await fetch(msgUrl, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  const msgData = (await msgRes.json()) as {
    threadId: string;
    payload?: { headers?: Array<{ name: string; value: string }> };
  };

  const headers = msgData.payload?.headers || [];
  const from = extractHeader(headers, "From");
  const subject = extractHeader(headers, "Subject");
  const messageIdHeader = extractHeader(headers, "Message-ID");
  const threadId = msgData.threadId;

  const replySubject = subject.startsWith("Re:") ? subject : `Re: ${subject}`;
  const rawEmail = buildRawEmail(from, replySubject, replyBody, messageIdHeader);
  const encodedMessage = encodeRawEmail(rawEmail);

  // 直接送信
  const sendUrl = "https://gmail.googleapis.com/gmail/v1/users/me/messages/send";
  const sendRes = await fetch(sendUrl, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${accessToken}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      raw: encodedMessage,
      threadId,
    }),
  });

  const sendData = (await sendRes.json()) as {
    id?: string;
    error?: unknown;
  };

  if (sendData.error) {
    throw new Error(`Send error: ${JSON.stringify(sendData.error)}`);
  }

  return [
    "✅ 返信を送信しました。",
    `宛先: ${from}`,
    `件名: ${replySubject}`,
    `Message ID: ${sendData.id}`,
  ].join("\n");
}

// --- ミラーサイト通知メールの送信失敗監視 ---

export interface MirrorBounce {
  id: string;
  date: string;
  subject: string;
  snippet: string;
}

/**
 * ミラーサイト通知メールの送信失敗を検知する。
 *
 * TC事業者ミラーの自動返信・事業者通知は takahashi@ の「Send mail as」エイリアス
 * (toiretsumari.center@gmail.com) 経由で送られる。そのアプリパスワードが失効すると、
 * GmailApp.sendEmail は実行時例外を出さないまま配信段階で 535 BadCredentials により
 * 全ミラーの送信がサイレントにバウンスする。mailer-daemon からの該当バウンスを抽出する。
 *
 * 背景: 2026-06-13、アプリPW失効で全ミラー通知が約21時間サイレント停止した事故の再発防止。
 */
export async function findMirrorBounces(
  windowHours: number = 2,
): Promise<MirrorBounce[]> {
  if (!GMAIL_CONFIG.refreshToken) {
    console.warn("[MirrorBounce] GMAIL_REFRESH_TOKEN 未設定のため監視をスキップ。");
    return [];
  }

  const accessToken = await getAccessToken();
  const query = `from:mailer-daemon newer_than:${windowHours}h`;
  const listUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages?q=${encodeURIComponent(query)}&maxResults=20`;

  const listRes = await fetch(listUrl, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  const listData = (await listRes.json()) as {
    messages?: Array<{ id: string }>;
  };
  if (!listData.messages || listData.messages.length === 0) return [];

  // エイリアスのアプリPW失効を示すバウンスのマーカー
  const FAILURE_MARKERS =
    /BadCredentials|Username and Password not accepted|5\.7\.8/i;

  const bounces: MirrorBounce[] = [];
  for (const msg of listData.messages) {
    const msgUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages/${msg.id}?format=full`;
    const msgRes = await fetch(msgUrl, {
      headers: { Authorization: `Bearer ${accessToken}` },
    });
    const msgData = (await msgRes.json()) as {
      id: string;
      threadId: string;
      snippet?: string;
      payload?: Record<string, unknown>;
    };

    const headers =
      (msgData.payload?.headers as Array<{ name: string; value: string }>) ||
      [];
    const subject = extractHeader(headers, "Subject");
    const body = msgData.payload ? extractBody(msgData.payload) : "";
    const haystack = `${subject}\n${msgData.snippet || ""}\n${body}`;

    if (!FAILURE_MARKERS.test(haystack)) continue;

    bounces.push({
      id: msgData.id,
      date: extractHeader(headers, "Date"),
      subject,
      snippet: (msgData.snippet || body).slice(0, 200),
    });
  }

  return bounces;
}
