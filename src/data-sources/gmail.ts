// メルマガ・通知と判定する送信元パターン
const NOISE_SENDER_PATTERNS = [
  /noreply@/i,
  /no-reply@/i,
  /do-not-reply@/i,
  /donotreply@/i,
  /notifications?@/i,
  /notify@/i,
  /alert@/i,
  /mailer-daemon@/i,
  /postmaster@/i,
  /news(letter)?@/i,
  /info@.*\.co\.jp/i, // info@ は大半がメルマガ
  /support@.*\.(com|io|dev)/i, // SaaS系サポート通知
  /updates?@/i,
  /digest@/i,
  /billing@/i,
  /receipt@/i,
];

// メルマガ・自動通知と判定するヘッダー
function isAutoGenerated(
  headers: Array<{ name: string; value: string }>,
): boolean {
  const hasListUnsubscribe = headers.some(
    (h) => h.name.toLowerCase() === "list-unsubscribe",
  );
  const precedence = headers
    .find((h) => h.name.toLowerCase() === "precedence")
    ?.value?.toLowerCase();
  const autoSubmitted = headers
    .find((h) => h.name.toLowerCase() === "auto-submitted")
    ?.value?.toLowerCase();
  const listId = headers.some((h) => h.name.toLowerCase() === "list-id");

  return (
    hasListUnsubscribe ||
    listId ||
    precedence === "bulk" ||
    precedence === "list" ||
    (autoSubmitted !== undefined && autoSubmitted !== "no")
  );
}

function isNoiseSender(from: string): boolean {
  return NOISE_SENDER_PATTERNS.some((pattern) => pattern.test(from));
}

export interface FilteredResult {
  actionable: GmailEmail[];
  filtered: Array<{ subject: string; from: string; reason: string }>;
}

// --- Helpers ---

export interface GmailEmail {
  id: string;
  threadId: string;
  from: string;
  to: string;
  subject: string;
  date: string;
  body: string;
}

function decodeBase64Url(str: string): string {
  const base64 = str.replace(/-/g, "+").replace(/_/g, "/");
  return Buffer.from(base64, "base64").toString("utf-8");
}

function extractHeader(
  headers: Array<{ name: string; value: string }>,
  name: string,
): string {
  return (
    headers.find((h) => h.name.toLowerCase() === name.toLowerCase())?.value ||
    ""
  );
}

function extractBody(payload: Record<string, unknown>): string {
  const mimeType = payload.mimeType as string | undefined;
  const body = payload.body as { data?: string } | undefined;
  const parts = payload.parts as Record<string, unknown>[] | undefined;

  if (mimeType === "text/plain" && body?.data) {
    return decodeBase64Url(body.data);
  }

  if (parts) {
    for (const part of parts) {
      const partBody = part.body as { data?: string } | undefined;
      if (part.mimeType === "text/plain" && partBody?.data) {
        return decodeBase64Url(partBody.data);
      }
    }
    for (const part of parts) {
      const partBody = part.body as { data?: string } | undefined;
      if (part.mimeType === "text/html" && partBody?.data) {
        const html = decodeBase64Url(partBody.data);
        return html
          .replace(/<[^>]*>/g, " ")
          .replace(/&nbsp;/g, " ")
          .replace(/\s+/g, " ")
          .trim();
      }
    }
    for (const part of parts) {
      if (part.parts) {
        const nested = extractBody(part);
        if (nested) return nested;
      }
    }
  }

  return "";
}

// RFC 2822 メール構築ヘルパー
function buildRawEmail(
  to: string,
  subject: string,
  body: string,
  inReplyTo?: string,
  references?: string,
): string {
  const lines = [
    `To: ${to}`,
    `Subject: ${subject}`,
    ...(inReplyTo ? [`In-Reply-To: ${inReplyTo}`, `References: ${references || inReplyTo}`] : []),
    `Content-Type: text/plain; charset=UTF-8`,
    "",
    body,
  ];
  return lines.join("\r\n");
}

function encodeRawEmail(raw: string): string {
  return Buffer.from(raw)
    .toString("base64")
    .replace(/\+/g, "-")
    .replace(/\//g, "_")
    .replace(/=+$/, "");
}

// --- メッセージ取得（共通） ---

async function fetchMessages(
  accessToken: string,
  query: string,
  maxResults: number,
): Promise<{ emails: GmailEmail[]; filtered: FilteredResult["filtered"] }> {
  const listUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages?q=${encodeURIComponent(query)}&maxResults=${maxResults}`;

  const listRes = await fetch(listUrl, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  const listData = (await listRes.json()) as {
    messages?: Array<{ id: string }>;
  };

  if (!listData.messages || listData.messages.length === 0) {
    return { emails: [], filtered: [] };
  }

  const emails: GmailEmail[] = [];
  const filtered: FilteredResult["filtered"] = [];

  for (const msg of listData.messages) {
    const msgUrl = `https://gmail.googleapis.com/gmail/v1/users/me/messages/${msg.id}?format=full`;
    const msgRes = await fetch(msgUrl, {
      headers: { Authorization: `Bearer ${accessToken}` },
    });
    const msgData = (await msgRes.json()) as {
      id: string;
      threadId: string;
      snippet?: string;
      payload?: Record<string, unknown>;
    };

    const headers =
      (msgData.payload?.headers as Array<{ name: string; value: string }>) ||
      [];
    const from = extractHeader(headers, "From");
    const subject = extractHeader(headers, "Subject");

    // 一次フィルタリング: メルマガ・通知を除外
    if (isAutoGenerated(headers)) {
      filtered.push({ subject, from, reason: "自動通知/メルマガ（ヘッダー判定）" });
      continue;
    }
    if (isNoiseSender(from)) {
      filtered.push({ subject, from, reason: "通知系送信元" });
      continue;
    }

    const body = msgData.payload ? extractBody(msgData.payload) : "";

    emails.push({
      id: msgData.id,
      threadId: msgData.threadId,
      from,
      to: extractHeader(headers, "To"),
      subject,
      date: extractHeader(headers, "Date"),
      body: body.slice(0, 1500),
    });
  }

  return { emails, filtered };
}

